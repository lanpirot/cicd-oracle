package ch.unibe.cs.mergeci.experiment;

import ch.unibe.cs.mergeci.config.AppConfig;
import com.fasterxml.jackson.databind.ObjectMapper;

import java.io.BufferedReader;
import java.io.FileReader;
import java.io.IOException;
import java.nio.file.Path;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Comparator;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;
import java.util.Random;
import java.util.Set;

/**
 * Reads maven_conflicts.csv (one row per merge) and produces a round-robin
 * ordered list of {@link DatasetReader.MergeInfo} suitable for both RQ2 and RQ3.
 *
 * <p>maven_conflicts.csv is generated by {@code extract_from_sql_dump.py} from
 * the SQL dump. Each row contains exact commit SHAs directly from the database,
 * with no ambiguity from GitHub API date-matching.
 *
 * <p>Sampling order ({@link #sampleRoundRobin}):
 * <ol>
 *   <li>Filter rows to {@code is_maven = True} and (when available) the fold-assigned set.</li>
 *   <li>Group by project; within each project, sort merges by {@code commit_time} desc.</li>
 *   <li>Shuffle the project order with a fixed seed (42) so it is deterministic and
 *       independent of CSV row order.</li>
 *   <li>Emit merges round-robin: for round {@code k=0,1,2,…}, walk the shuffled
 *       project list and append each project's {@code k}-th-youngest merge if it
 *       has one. Stop when no project has a {@code k}-th merge to contribute.</li>
 * </ol>
 *
 * <p>The pipeline (RQPipelineRunner) consumes this queue lazily and stops once
 * {@code processedLimit()} successful merges are recorded.
 */
public class JavaChunksReader {

    /**
     * Round-robin sample of every Maven merge in the CSV.
     *
     * <p>Round 1 returns one merge per project (the youngest) in shuffled-project
     * order. Round 2 returns the second-youngest of every project that has one.
     * Etc. Total length = sum over projects of {@code min(merges, ∞) }, i.e.
     * every fold-assigned Maven merge in the corpus.
     *
     * @param csvPath path to maven_conflicts.csv
     * @return ordered list of {@link DatasetReader.MergeInfo} with mergeCommit,
     *         mergeId, remoteUrl, and projectName populated
     */
    public List<DatasetReader.MergeInfo> sampleRoundRobin(Path csvPath) throws IOException {
        Set<String> foldIds = loadFoldAssignedIds();
        String debugCommit = System.getProperty("debugMergeCommit", "").trim();
        String debugProject = System.getProperty("debugProjectName", "").trim();

        Map<String, List<MergeRow>> byProject = new LinkedHashMap<>();

        try (BufferedReader reader = new BufferedReader(new FileReader(csvPath.toFile()))) {
            String headerLine = reader.readLine();
            if (headerLine == null) return List.of();

            String[] headers = parseCsvLine(headerLine);
            int idxMergeId    = requireColumn(headers, "merge_id", csvPath);
            int idxCommitId   = requireColumn(headers, "commit_id", csvPath);
            int idxProject    = requireColumn(headers, "project_name", csvPath);
            int idxRemoteUrl  = requireColumn(headers, "remote_url", csvPath);
            int idxCommitTime = requireColumn(headers, "commit_time", csvPath);
            int idxIsMaven    = requireColumn(headers, "is_maven", csvPath);

            String line;
            while ((line = reader.readLine()) != null) {
                if (line.isBlank()) continue;
                String[] fields = parseCsvLine(line);
                if (fields.length <= idxIsMaven) continue;
                if (!"True".equalsIgnoreCase(fields[idxIsMaven].trim())) continue;

                String mergeId    = fields[idxMergeId].trim();
                if (!foldIds.isEmpty() && !foldIds.contains(mergeId)) continue;

                String projectName = fields[idxProject].trim();
                String commitId    = fields[idxCommitId].trim();
                String commitTime  = fields[idxCommitTime].trim();
                String remoteUrl   = fields[idxRemoteUrl].trim();

                if (!debugCommit.isEmpty() && !commitId.startsWith(debugCommit)) continue;
                if (!debugProject.isEmpty() && !projectName.equals(debugProject)) continue;

                byProject.computeIfAbsent(projectName, k -> new ArrayList<>())
                        .add(new MergeRow(mergeId, commitTime, remoteUrl, commitId, projectName));
            }
        }

        List<Map.Entry<String, List<MergeRow>>> projects = new ArrayList<>(byProject.entrySet());
        if (projects.isEmpty()) return List.of();

        Collections.shuffle(projects, new Random(42));

        // Sort each project's merges youngest-first so round k draws the k-th-youngest.
        for (Map.Entry<String, List<MergeRow>> entry : projects) {
            entry.getValue().sort(Comparator.comparing((MergeRow r) -> r.mergeTime).reversed());
        }

        int maxDepth = projects.stream().mapToInt(e -> e.getValue().size()).max().orElse(0);
        List<DatasetReader.MergeInfo> result = new ArrayList<>();
        for (int round = 0; round < maxDepth; round++) {
            for (Map.Entry<String, List<MergeRow>> entry : projects) {
                List<MergeRow> rows = entry.getValue();
                if (round < rows.size()) {
                    result.add(toMergeInfo(rows.get(round)));
                }
            }
        }
        return result;
    }

    private static DatasetReader.MergeInfo toMergeInfo(MergeRow row) {
        if (row.mergeCommit.isEmpty()) {
            throw new IllegalStateException(
                "commit_id column is blank for merge_id=" + row.mergeId
                + " in project=" + row.projectName
                + ". Run extract_from_sql_dump.py first.");
        }
        DatasetReader.MergeInfo info = new DatasetReader.MergeInfo();
        info.setMergeCommit(row.mergeCommit);
        info.setMergeId(row.mergeId);
        info.setRemoteUrl(row.remoteUrl);
        info.setProjectName(row.projectName);
        return info;
    }

    private static int requireColumn(String[] headers, String name, Path csvPath) {
        int idx = indexOf(headers, name);
        if (idx < 0) throw new IllegalStateException(
            "Required column '" + name + "' not found in " + csvPath
            + ". Columns present: " + String.join(", ", headers));
        return idx;
    }

    private static int indexOf(String[] headers, String name) {
        for (int i = 0; i < headers.length; i++) {
            if (headers[i].trim().equalsIgnoreCase(name)) return i;
        }
        return -1;
    }

    private static String[] parseCsvLine(String line) {
        List<String> result = new ArrayList<>();
        StringBuilder field = new StringBuilder();
        boolean inQuotes = false;
        for (int i = 0; i < line.length(); i++) {
            char c = line.charAt(i);
            if (c == '"') {
                if (inQuotes && i + 1 < line.length() && line.charAt(i + 1) == '"') {
                    field.append('"');
                    i++;
                } else {
                    inQuotes = !inQuotes;
                }
            } else if (c == ',' && !inQuotes) {
                result.add(field.toString());
                field = new StringBuilder();
            } else {
                field.append(c);
            }
        }
        result.add(field.toString());
        return result.toArray(new String[0]);
    }

    /**
     * Load the set of merge IDs that have a fold assignment (i.e. are in the ML-AR
     * evaluation set). Merges absent from this file cannot be run through the ML-AR
     * generator (predict_mlar.py hard-errors on unknown IDs).
     *
     * @return set of merge_id strings, or an empty set with a warning if the file is missing
     */
    static Set<String> loadFoldAssignedIds() {
        Path assignmentFile = AppConfig.RQ1_FOLD_ASSIGNMENT_FILE;
        if (!assignmentFile.toFile().exists()) {
            System.err.printf("Warning: fold assignment file not found at %s — fold filtering skipped%n", assignmentFile);
            return Set.of();
        }
        try {
            @SuppressWarnings("unchecked")
            Map<String, Object> map = new ObjectMapper().readValue(assignmentFile.toFile(), Map.class);
            return map.keySet();
        } catch (IOException e) {
            System.err.printf("Warning: could not read fold assignment file %s: %s — fold filtering skipped%n", assignmentFile, e.getMessage());
            return Set.of();
        }
    }

    private record MergeRow(String mergeId, String mergeTime, String remoteUrl, String mergeCommit, String projectName) {}
}
