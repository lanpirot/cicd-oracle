package ch.unibe.cs.mergeci.experiment;

import ch.unibe.cs.mergeci.config.AppConfig;
import com.fasterxml.jackson.databind.ObjectMapper;

import java.io.BufferedReader;
import java.io.FileReader;
import java.io.IOException;
import java.nio.file.Path;
import java.util.ArrayList;
import java.util.Collections;
import java.util.Comparator;
import java.util.LinkedHashMap;
import java.util.List;
import java.util.Map;
import java.util.Random;
import java.util.Set;

/**
 * Reads maven_conflicts.csv (one row per merge) and produces a sampled list of
 * {@link DatasetReader.MergeInfo} objects suitable for the RQ2 pipeline.
 *
 * <p>maven_conflicts.csv is generated by {@code extract_from_sql_dump.py} from
 * the SQL dump. Each row contains exact commit SHAs directly from the database,
 * with no ambiguity from GitHub API date-matching.
 *
 * <p>Sampling logic:
 * <ol>
 *   <li>Filter rows to {@code is_maven = True}</li>
 *   <li>Per project: sort merges by {@code commit_time} descending (youngest first)</li>
 *   <li>Take at most {@code mergesPerRepo} merges per project</li>
 *   <li>Take at most {@code maxRepos} distinct projects (in encounter order)</li>
 * </ol>
 */
public class JavaChunksReader {

    /**
     * Sample merges from maven_conflicts.csv.
     *
     * @param csvPath      path to maven_conflicts.csv
     * @param maxRepos     maximum number of distinct projects to include
     * @param mergesPerRepo maximum number of merges to include per project
     * @return list of {@link DatasetReader.MergeInfo} with mergeCommit, mergeId,
     *         remoteUrl, and projectName populated (parent1/parent2 resolved later)
     */
    public List<DatasetReader.MergeInfo> sample(Path csvPath, int maxRepos, int mergesPerRepo) throws IOException {
        Set<String> foldIds = loadFoldAssignedIds();

        // project_name → list of MergeRow — preserves first-seen project order
        Map<String, List<MergeRow>> byProject = new LinkedHashMap<>();

        try (BufferedReader reader = new BufferedReader(new FileReader(csvPath.toFile()))) {
            String headerLine = reader.readLine();
            if (headerLine == null) return List.of();

            String[] headers = parseCsvLine(headerLine);
            int idxMergeId    = requireColumn(headers, "merge_id", csvPath);
            int idxCommitId   = requireColumn(headers, "commit_id", csvPath);
            int idxProject    = requireColumn(headers, "project_name", csvPath);
            int idxRemoteUrl  = requireColumn(headers, "remote_url", csvPath);
            int idxCommitTime = requireColumn(headers, "commit_time", csvPath);
            int idxIsMaven    = requireColumn(headers, "is_maven", csvPath);

            String line;
            while ((line = reader.readLine()) != null) {
                if (line.isBlank()) continue;
                String[] fields = parseCsvLine(line);
                if (fields.length <= idxIsMaven) continue;

                if (!"True".equalsIgnoreCase(fields[idxIsMaven].trim())) continue;

                String mergeId    = fields[idxMergeId].trim();
                if (!foldIds.isEmpty() && !foldIds.contains(mergeId)) continue;

                String projectName = fields[idxProject].trim();
                String commitId    = fields[idxCommitId].trim();
                String commitTime  = fields[idxCommitTime].trim();
                String remoteUrl   = fields[idxRemoteUrl].trim();

                byProject.computeIfAbsent(projectName, k -> new ArrayList<>())
                        .add(new MergeRow(mergeId, commitTime, remoteUrl, commitId, projectName));
            }
        }

        List<Map.Entry<String, List<MergeRow>>> projects = new ArrayList<>(byProject.entrySet());
        Collections.shuffle(projects, new Random(42));

        List<DatasetReader.MergeInfo> result = new ArrayList<>();
        int repoCount = 0;

        for (Map.Entry<String, List<MergeRow>> entry : projects) {
            if (repoCount >= maxRepos) break;

            List<MergeRow> rows = new ArrayList<>(entry.getValue());
            rows.sort(Comparator.comparing((MergeRow r) -> r.mergeTime).reversed());

            int taken = 0;
            for (MergeRow row : rows) {
                if (taken >= mergesPerRepo) break;
                result.add(toMergeInfo(row));
                taken++;
            }
            repoCount++;
        }

        return result;
    }

    /**
     * Distribute {@code totalTarget} merges fairly across all Maven projects.
     *
     * <p>Algorithm:
     * <ol>
     *   <li>Load all Maven merges grouped by project (same CSV as {@link #sample})</li>
     *   <li>Compute {@code fairShare = ceil(totalTarget / numProjects)}</li>
     *   <li>First pass: each project contributes {@code min(available, fairShare)}</li>
     *   <li>If total &lt; totalTarget: sort projects with remaining merges by count descending,
     *       round-robin +1 until totalTarget reached or all exhausted</li>
     * </ol>
     *
     * @param csvPath     path to maven_conflicts.csv
     * @param totalTarget total number of merges to include (≤ available Maven merges)
     * @return list of up to {@code totalTarget} {@link DatasetReader.MergeInfo} objects
     */
    public List<DatasetReader.MergeInfo> sampleDistributed(Path csvPath, int totalTarget) throws IOException {
        Set<String> foldIds = loadFoldAssignedIds();

        Map<String, List<MergeRow>> byProject = new LinkedHashMap<>();

        try (BufferedReader reader = new BufferedReader(new FileReader(csvPath.toFile()))) {
            String headerLine = reader.readLine();
            if (headerLine == null) return List.of();

            String[] headers = parseCsvLine(headerLine);
            int idxMergeId    = requireColumn(headers, "merge_id", csvPath);
            int idxCommitId   = requireColumn(headers, "commit_id", csvPath);
            int idxProject    = requireColumn(headers, "project_name", csvPath);
            int idxRemoteUrl  = requireColumn(headers, "remote_url", csvPath);
            int idxCommitTime = requireColumn(headers, "commit_time", csvPath);
            int idxIsMaven    = requireColumn(headers, "is_maven", csvPath);

            String line;
            while ((line = reader.readLine()) != null) {
                if (line.isBlank()) continue;
                String[] fields = parseCsvLine(line);
                if (fields.length <= idxIsMaven) continue;
                if (!"True".equalsIgnoreCase(fields[idxIsMaven].trim())) continue;

                String mergeId    = fields[idxMergeId].trim();
                if (!foldIds.isEmpty() && !foldIds.contains(mergeId)) continue;

                String projectName = fields[idxProject].trim();
                String commitId    = fields[idxCommitId].trim();
                String commitTime  = fields[idxCommitTime].trim();
                String remoteUrl   = fields[idxRemoteUrl].trim();

                byProject.computeIfAbsent(projectName, k -> new ArrayList<>())
                        .add(new MergeRow(mergeId, commitTime, remoteUrl, commitId, projectName));
            }
        }

        // Shuffle projects with a fixed seed so encounter order in the CSV does not
        // affect which projects receive the round-robin top-up in pass 2.
        List<Map.Entry<String, List<MergeRow>>> projectEntries = new ArrayList<>(byProject.entrySet());
        Collections.shuffle(projectEntries, new Random(42));

        // Sort each project's merges youngest-first
        for (Map.Entry<String, List<MergeRow>> entry : projectEntries) {
            entry.getValue().sort(Comparator.comparing((MergeRow r) -> r.mergeTime).reversed());
        }

        int numProjects = projectEntries.size();
        if (numProjects == 0) return List.of();

        int fairShare = (int) Math.ceil((double) totalTarget / numProjects);

        // Track how many from each project were already taken and what remains
        Map<String, Integer> takenPerProject = new LinkedHashMap<>();
        List<DatasetReader.MergeInfo> result = new ArrayList<>();

        // First pass: up to fairShare per project
        for (Map.Entry<String, List<MergeRow>> entry : projectEntries) {
            String project = entry.getKey();
            List<MergeRow> rows = entry.getValue();
            int take = Math.min(rows.size(), fairShare);
            takenPerProject.put(project, take);
            for (int i = 0; i < take; i++) {
                result.add(toMergeInfo(rows.get(i)));
            }
        }

        // Second pass: fill up to totalTarget from projects with remaining merges
        if (result.size() < totalTarget) {
            // Sort projects by remaining-merge count descending
            List<Map.Entry<String, List<MergeRow>>> withRemainder = new ArrayList<>();
            for (Map.Entry<String, List<MergeRow>> entry : projectEntries) {
                int taken = takenPerProject.get(entry.getKey());
                if (taken < entry.getValue().size()) {
                    withRemainder.add(entry);
                }
            }
            withRemainder.sort((a, b) -> {
                int remA = a.getValue().size() - takenPerProject.get(a.getKey());
                int remB = b.getValue().size() - takenPerProject.get(b.getKey());
                return Integer.compare(remB, remA);
            });

            // Round-robin until target reached or exhausted
            boolean anyAdded = true;
            while (result.size() < totalTarget && anyAdded) {
                anyAdded = false;
                for (Map.Entry<String, List<MergeRow>> entry : withRemainder) {
                    if (result.size() >= totalTarget) break;
                    String project = entry.getKey();
                    int taken = takenPerProject.get(project);
                    if (taken < entry.getValue().size()) {
                        result.add(toMergeInfo(entry.getValue().get(taken)));
                        takenPerProject.put(project, taken + 1);
                        anyAdded = true;
                    }
                }
            }
        }

        return result;
    }

    private static DatasetReader.MergeInfo toMergeInfo(MergeRow row) {
        if (row.mergeCommit.isEmpty()) {
            throw new IllegalStateException(
                "commit_id column is blank for merge_id=" + row.mergeId
                + " in project=" + row.projectName
                + ". Run extract_from_sql_dump.py first.");
        }
        DatasetReader.MergeInfo info = new DatasetReader.MergeInfo();
        info.setMergeCommit(row.mergeCommit);
        info.setMergeId(row.mergeId);
        info.setRemoteUrl(row.remoteUrl);
        info.setProjectName(row.projectName);
        return info;
    }

    private static int requireColumn(String[] headers, String name, Path csvPath) {
        int idx = indexOf(headers, name);
        if (idx < 0) throw new IllegalStateException(
            "Required column '" + name + "' not found in " + csvPath
            + ". Columns present: " + String.join(", ", headers));
        return idx;
    }

    private static int indexOf(String[] headers, String name) {
        for (int i = 0; i < headers.length; i++) {
            if (headers[i].trim().equalsIgnoreCase(name)) return i;
        }
        return -1;
    }

    private static String[] parseCsvLine(String line) {
        List<String> result = new ArrayList<>();
        StringBuilder field = new StringBuilder();
        boolean inQuotes = false;
        for (int i = 0; i < line.length(); i++) {
            char c = line.charAt(i);
            if (c == '"') {
                if (inQuotes && i + 1 < line.length() && line.charAt(i + 1) == '"') {
                    field.append('"');
                    i++;
                } else {
                    inQuotes = !inQuotes;
                }
            } else if (c == ',' && !inQuotes) {
                result.add(field.toString());
                field = new StringBuilder();
            } else {
                field.append(c);
            }
        }
        result.add(field.toString());
        return result.toArray(new String[0]);
    }

    /**
     * Load the set of merge IDs that have a fold assignment (i.e. are in the ML-AR
     * evaluation set). Merges absent from this file cannot be run through the ML-AR
     * generator (predict_mlar.py hard-errors on unknown IDs).
     *
     * @return set of merge_id strings, or an empty set with a warning if the file is missing
     */
    static Set<String> loadFoldAssignedIds() {
        Path assignmentFile = AppConfig.RQ1_FOLD_ASSIGNMENT_FILE;
        if (!assignmentFile.toFile().exists()) {
            System.err.printf("Warning: fold assignment file not found at %s — fold filtering skipped%n", assignmentFile);
            return Set.of();
        }
        try {
            @SuppressWarnings("unchecked")
            Map<String, Object> map = new ObjectMapper().readValue(assignmentFile.toFile(), Map.class);
            return map.keySet();
        } catch (IOException e) {
            System.err.printf("Warning: could not read fold assignment file %s: %s — fold filtering skipped%n", assignmentFile, e.getMessage());
            return Set.of();
        }
    }

    private record MergeRow(String mergeId, String mergeTime, String remoteUrl, String mergeCommit, String projectName) {}
}
